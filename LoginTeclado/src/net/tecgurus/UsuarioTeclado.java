public class UsuarioTeclado {
	private String usr;
	private String pass;
	
	/**
	 * @return the usr
	 */
	public String getUsr() {
		return usr;
	}
	/**
	 * @param usr the usr to set
	 */
	public void setUsr(String usr) {
		this.usr = usr;
	}
	/**
	 * @return the pass
	 */
	public String getPass() {
		return pass;
	}
	/**
	 * @param pass the pass to set
	 */
	public void setPass(String pass) {
		this.pass = pass;
	}
}